import fs from "node:fs/promises";
import path from "node:path";
import sharp from "sharp";

const ROOT = process.cwd();
const MGT_DIR = path.join(ROOT, "public", "images", "mgt");
const OUT_DIR = path.join(MGT_DIR, "optimized");
const MANIFEST_PATH = path.join(ROOT, "src", "lib", "leadership-images.ts");

/** Portrait 4:5 — consistent executive presentation */
const PORTRAIT_RATIO = { width: 4, height: 5 };
const WIDTHS = [320, 480, 640, 800, 960];
const DEFAULT_WIDTH = 800;
const QUALITY = 82;

const EXECUTIVES = [
  {
    input: "Karol-Kemp.png",
    slug: "karol-kemp",
    alt: "Karol Kemp, Founder and Chief Executive Officer of Alto Rich"
  },
  {
    input: "Olaniyi-Adeyemo.png",
    slug: "olaniyi-adeyemo",
    alt: "Olaniyi Adeyemo, Executive Director Operations and Member Success at Alto Rich"
  },
  {
    input: "Tamunotonye-Peterside.png",
    slug: "tamunotonye-peterside",
    alt: "Tamunotonye Peterside, Regional Director Business Development at Alto Rich"
  },
  {
    input: "Abubakar-Nasir.png",
    slug: "abubakar-nasir",
    alt: "Abubakar Nasir, Regional Director Government and Institutional Relations at Alto Rich"
  }
];

function heightForWidth(width) {
  return Math.round((width * PORTRAIT_RATIO.height) / PORTRAIT_RATIO.width);
}

async function exists(p) {
  try {
    await fs.access(p);
    return true;
  } catch {
    return false;
  }
}

async function optimizePortrait(inputPath, outputPath, width) {
  const height = heightForWidth(width);

  await sharp(inputPath)
    .rotate()
    .resize(width, height, { fit: "cover", position: "centre" })
    .webp({ quality: QUALITY, effort: 6 })
    .toFile(outputPath);

  const stat = await fs.stat(outputPath);
  return stat.size;
}

async function main() {
  await fs.mkdir(OUT_DIR, { recursive: true });

  const manifest = {};

  for (const exec of EXECUTIVES) {
    const input = path.join(MGT_DIR, exec.input);
    if (!(await exists(input))) {
      throw new Error(`Missing source: ${input}`);
    }

    const variants = [];

    for (const width of WIDTHS) {
      const filename = `${exec.slug}-${width}.webp`;
      const output = path.join(OUT_DIR, filename);
      const bytes = await optimizePortrait(input, output, width);
      variants.push({ width, src: `/images/mgt/optimized/${filename}`, bytes });
      console.log(`[mgt] ${filename} — ${(bytes / 1024).toFixed(1)} KB`);
    }

    manifest[exec.slug] = {
      slug: exec.slug,
      original: `/images/mgt/${exec.input}`,
      alt: exec.alt,
      aspectRatio: "4/5",
      defaultWidth: DEFAULT_WIDTH,
      defaultHeight: heightForWidth(DEFAULT_WIDTH),
      variants
    };
  }

  const ts = `/** Auto-generated by scripts/optimize-management-images.mjs — do not edit manually */
export type LeadershipImageVariant = {
  width: number;
  src: string;
  bytes: number;
};

export type LeadershipImageAsset = {
  slug: string;
  original: string;
  alt: string;
  aspectRatio: "4/5";
  defaultWidth: number;
  defaultHeight: number;
  variants: LeadershipImageVariant[];
};

export const LEADERSHIP_IMAGES = ${JSON.stringify(manifest, null, 2)} as const satisfies Record<string, LeadershipImageAsset>;

export type LeadershipImageSlug = keyof typeof LEADERSHIP_IMAGES;
`;

  await fs.writeFile(MANIFEST_PATH, ts, "utf8");
  console.log(`[mgt] Manifest written to ${path.relative(ROOT, MANIFEST_PATH)}`);
  console.log("[mgt] Original PNG files preserved in public/images/mgt/");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
